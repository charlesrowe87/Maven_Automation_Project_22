package Day3_043022;

public class If_else_condition {
    public static void main(String[] args) {

        //declare two integer values
        int a = 1;
        int b = 3;

        //print when a is less than b or print when a is equal to/greater than b
        if (a < b) {
            System.out.println("a is less than b");
        }else{
            System.out.println("a is greater or equal to b");
        }//end of if else

    }//end of main

}//end of java class
